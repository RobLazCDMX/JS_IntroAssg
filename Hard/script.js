'use strict';

// Add function
function add(numberOne, numberTwo) {
    const totalAdd = numberOne + numberTwo;
    return totalAdd;
}

console.log(add(100, 5));

// Subtract function
function subtract(numberOne, numberTwo) {
    const totalSubtract = numberOne - numberTwo;
    return totalSubtract;
}

console.log(subtract(100, 5));


// Multiply function
function multiply(numberOne, numberTwo) {
    const totalMultiply = numberOne * numberTwo;
    return totalMultiply;
}

console.log(multiply(100, 5));


// Divides function
function divide(numberOne, numberTwo) {
    const totalDivide = numberOne / numberTwo;
    return totalDivide;
}

console.log(divide(100, 5));
